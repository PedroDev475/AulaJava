import java.util.Scanner;

// Soma de números pares de 1 a 100
public class Exercicio2 {
    public static void main(String[] args) {
        int soma = 0;
        int num = 2;

        while (num <= 100) {
            soma += num;
            num += 2;
        }

        System.out.println("A soma dos números pares de 1 a 100 é: " + soma);
    }
    }

